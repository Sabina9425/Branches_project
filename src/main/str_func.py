def convert_to_upper_case(txt):
    """"This function converts letters to upper case
    :param txt: input words
    :return: converted words
    """
    return txt.upper()

def convert_every_first_letter_to_upper_case(txt):
    """"This fucntion converts every first letter of every word to upper case
    :param txt: input sentence
    :return: converted sentence
    """
    return txt.capwords()

